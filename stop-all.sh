#!/bin/bash

# ===================================================================
# stop-all.sh - 모든 환경 종료 스크립트
# ===================================================================
# 사용법: ./stop-all.sh
# 기능: 개발, 테스트, 프로덕션 환경의 Docker 컨테이너를 모두 종료합니다.
# ===================================================================

echo "모든 환경을 종료합니다..."

# 프로덕션 환경부터 역순으로 종료 (종속성 문제 방지)

# 프로덕션 환경 종료 - 프로덕션 환경 전용 스크립트 호출
echo "프로덕션 환경 종료 중..."
./docker/prod/stop.sh

# 테스트 환경 종료 - 테스트 환경 전용 스크립트 호출
echo "테스트 환경 종료 중..."
./docker/test/stop.sh

# 개발 환경 종료 - 개발 환경 전용 스크립트 호출
echo "개발 환경 종료 중..."
./docker/dev/stop.sh

echo "모든 환경이 종료되었습니다."